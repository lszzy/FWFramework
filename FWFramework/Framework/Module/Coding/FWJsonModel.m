FWEncodingType FWEncodingGetType(const char *typeEncoding) {
    char *type = (char *)typeEncoding;
    if (!type) return FWEncodingTypeUnknown;
    size_t len = strlen(type);
    if (len == 0) return FWEncodingTypeUnknown;
    
    FWEncodingType qualifier = 0;
    bool prefix = true;
    while (prefix) {
        switch (*type) {
            case 'r': {
                qualifier |= FWEncodingTypeQualifierConst;
                type++;
            } break;
            case 'n': {
                qualifier |= FWEncodingTypeQualifierIn;
                type++;
            } break;
            case 'N': {
                qualifier |= FWEncodingTypeQualifierInout;
                type++;
            } break;
            case 'o': {
                qualifier |= FWEncodingTypeQualifierOut;
                type++;
            } break;
            case 'O': {
                qualifier |= FWEncodingTypeQualifierBycopy;
                type++;
            } break;
            case 'R': {
                qualifier |= FWEncodingTypeQualifierByref;
                type++;
            } break;
            case 'V': {
                qualifier |= FWEncodingTypeQualifierOneway;
                type++;
            } break;
            default: { prefix = false; } break;
        }
    }
    
    len = strlen(type);
    if (len == 0) return FWEncodingTypeUnknown | qualifier;
    
    switch (*type) {
        case 'v': return FWEncodingTypeVoid | qualifier;
        case 'B': return FWEncodingTypeBool | qualifier;
        case 'c': return FWEncodingTypeInt8 | qualifier;
        case 'C': return FWEncodingTypeUInt8 | qualifier;
        case 's': return FWEncodingTypeInt16 | qualifier;
        case 'S': return FWEncodingTypeUInt16 | qualifier;
        case 'i': return FWEncodingTypeInt32 | qualifier;
        case 'I': return FWEncodingTypeUInt32 | qualifier;
        case 'l': return FWEncodingTypeInt32 | qualifier;
        case 'L': return FWEncodingTypeUInt32 | qualifier;
        case 'q': return FWEncodingTypeInt64 | qualifier;
        case 'Q': return FWEncodingTypeUInt64 | qualifier;
        case 'f': return FWEncodingTypeFloat | qualifier;
        case 'd': return FWEncodingTypeDouble | qualifier;
        case 'D': return FWEncodingTypeLongDouble | qualifier;
        case '#': return FWEncodingTypeClass | qualifier;
        case ':': return FWEncodingTypeSEL | qualifier;
        case '*': return FWEncodingTypeCString | qualifier;
        case '^': return FWEncodingTypePointer | qualifier;
        case '[': return FWEncodingTypeCArray | qualifier;
        case '(': return FWEncodingTypeUnion | qualifier;
        case '{': return FWEncodingTypeStruct | qualifier;
        case '@': {
            if (len == 2 && *(type + 1) == '?')
                return FWEncodingTypeBlock | qualifier;
            else
                return FWEncodingTypeObject | qualifier;
        }
        default: return FWEncodingTypeUnknown | qualifier;
    }
}

